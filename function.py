# 1 Function defination 
# Function defination is one time process
def addition(fomalarg1,fomalarg2):
    result = fomalarg1 + fomalarg2
    print(f"The addition of {fomalarg1} and {fomalarg2} is{result}")

# 2 Function Calling/Invoking
# Function calling is many time process
addition(5,5) # actualarg1=5,actualarg2=5
addition(10,5) # actualarg1=10,actualarg2=5
addition(20,20) # actualarg1=20,actualarg2=20
